function generateImageObjects(urls, category, prefix) {
  return urls.map((url, index) => ({
    id: `${prefix}-${index + 1}`,
    url: url,
    title: `${category} ${index + 1}`,
    category: category,
    description: `${category} aesthetic flatlay`
  }));
}

// Generate Editorial Magazine collection
