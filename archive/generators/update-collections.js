console.log(`✅ Editorial Magazine: ${editorialMagazineImages.length} images generated`);

// Read current flatlay-library.tsx
const filePath = './client/src/pages/flatlay-library.tsx';
let content = fs.readFileSync(filePath, 'utf8');

// Replace the Editorial Magazine collection images array 
const startMarker = "backgroundImage: 'https://i.postimg.cc/02VLGyr8/1.png',\n    images: [";
const endMarker = "    ]\n  },\n  {\n    id: 'european-luxury',";

const startIndex = content.indexOf(startMarker);
const endIndex = content.indexOf(endMarker);

if (startIndex !== -1 && endIndex !== -1) {
  const before = content.substring(0, startIndex + startMarker.length);
  const after = content.substring(endIndex);
  
  // Generate the new images array content
  const imagesContent = editorialMagazineImages.map(img => 
    `      {\n        id: '${img.id}',\n        url: '${img.url}',\n        title: '${img.title}',\n        category: '${img.category}',\n        description: '${img.description}'\n      }`
  ).join(',\n');
  
  const newContent = before + '\n' + imagesContent + '\n' + after;
  fs.writeFileSync(filePath, newContent);
  
  console.log(`✅ Successfully updated Editorial Magazine collection with ${editorialMagazineImages.length} images`);
} else {
  console.log('❌ Could not find Editorial Magazine collection to replace');
}